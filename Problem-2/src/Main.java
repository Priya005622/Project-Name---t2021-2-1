public class Main {
    public static void Series(int a) {
        for (int i = 1; i <= a; i++) {
            int number = 2 * i - 1;
            System.out.print(number + " ");
        }
        System.out.println();
    }
    public static void main(String[] args) {
        int a = 3;
        Series(a);
    }
}




